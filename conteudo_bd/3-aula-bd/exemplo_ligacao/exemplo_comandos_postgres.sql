CREATE TABLE FUNCIONARIOS (
	id_funcionario SERIAL NOT NULL PRIMARY KEY,
	nome_funcionario VARCHAR(100) NOT NULL,
	status_funcionario BOOLEAN NOT NULL,
	cidade_funcionario VARCHAR(50) 
);

CREATE TABLE PRODUTOS (
	id_produtos INT NOT NULL PRIMARY KEY,
	nome_produtos varchar(255) NOT NULL,
	qtde_produtos INT NOT NULL,
	valor_produtos MONEY NOT NULL,
	validade_produtos date NOT NULL
	FOREIGN KEY id_funcionarios REFERENCES FUNCIONARIOS -- Chave estrangeira
);

